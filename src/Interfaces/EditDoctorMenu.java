    private void RemoveDayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveDayButtonActionPerformed
        String name = (String)AvailableDaysComboBox.getSelectedItem();
        if(name != null) {
            int day = Integer.parseInt(name);
            String doctor = (String)DoctorListComboBox.getSelectedItem();
            
            Pair<String, Integer> pair = new Pair(doctor, day);
            GuiEvent ge = new GuiEvent(pair, REMOVE_DAY);
            source.postGuiEvent(ge);
            
            this.dispose();
        }
        else JOptionPane.showMessageDialog(this, "Não há dias selecionados!", "Remover Dia", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_RemoveDayButtonActionPerformed

    private void RemoveDoctorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveDoctorButtonActionPerformed
        String name = (String)DoctorListComboBox.getSelectedItem();
        if(name != null) {
            GuiEvent ge = new GuiEvent(name, REMOVE_DOCTOR);
            source.postGuiEvent(ge);
            this.dispose();
        }
        else JOptionPane.showMessageDialog(this, "Não há médicos selecionados!", "Remover Médico", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_RemoveDoctorButtonActionPerformed

