    private void AvailableDaysButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AvailableDaysButtonActionPerformed
        AvailableDaysComboBox.removeAllItems();
        String name = (String)DoctorListComboBox.getSelectedItem();
        if(name != null) {
            for(Doctor d : doctors.values()) {
                if(d.getName().equals(name)) {
                    for(Day day : d.getAvailableDays().values()) AvailableDaysComboBox.addItem("" + day.getDay());
                    break;
                }
            }
        }
        else JOptionPane.showMessageDialog(this, "Não há médicos selecionados!", "Dias Disponíveis", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_AvailableDaysButtonActionPerformed

